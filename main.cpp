#include <iostream>

using namespace std;

int main()
{
    int a = 123456789;
    float b = 3.14;
    double c = 3.99;
    string d = "One, Two, Three";
    char e = 'X';
    bool f = false;
    bool g = true;

    cout << a << " = Interger" << endl;
    cout << b << " = Floating" << endl;
    cout << c << " = Floating but Double" << endl;
    cout << d << " = String" << endl;
    cout << e << " = Character" << endl;
    cout << f << " = False Boolean" << endl;
    cout << g << " = True Boolean" << endl;
    return 0;
}
